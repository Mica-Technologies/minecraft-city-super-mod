package com.micatechnologies.minecraft.csm.trafficsignals;

/**
 * Enum of actions that can be performed on a crosswalk signal via the config GUI.
 * Each value corresponds to a cycle/toggle operation on the tile entity.
 */
public enum CrosswalkConfigAction {
    CYCLE_BODY_COLOR,
    CYCLE_VISOR_COLOR,
    CYCLE_VISOR_TYPE,
    CYCLE_MOUNT_TYPE,
    CYCLE_BODY_TILT,
    CYCLE_BULB_TYPE
}
